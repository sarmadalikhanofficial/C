#include<stdio.h>
int main()
{
//Condition: s <= 100
//This is the condition that must be true for the loop to continue running.
//As long as s is less than or equal to 100, the loop will keep executing.
//Once s becomes greater than 100, the loop will stop and the program will exit the loop.

//s++ means s = s + 1. It increases the value of s by 1 after each iteration.
//This ensures that the loop progresses from 1 to 100, one step at a time.

for (int s=1; s<=100; s++)
 	{
	 printf("%d\n", s*10);
 	}
}
